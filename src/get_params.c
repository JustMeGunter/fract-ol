#include <fractol.h>

void	get_sfsc(int x, int y, t_sfsc *sfsc)
{
	sfsc->shftd_x =  x / 2.0;
	sfsc->shftd_y =  y / 2.0;
	sfsc->scld_x = 4.0 / x;
	sfsc->scld_y = 4.0 / y;
}
